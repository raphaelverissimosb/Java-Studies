
public class FlowControl4 {

	public static void main(String[] args) {
		// while , do while and for 
		int counter  =  0;
		
		while(counter < 10) {
			System.out.println(counter);
			counter ++;
		}
		
		do {
			System.out.println(counter);
		}while(counter < 10);
		
		
		for(int i = 0 ; i < counter;i++) {
			System.out.println(counter);
		}

	}

}
